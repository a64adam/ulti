package dto.staticdata;

public class Image {
    private String full;
    private String group;
    private int h;
    private String sprite;
    private int w;
    private int x;
    private int y;

    public String getFull() {
        return full;
    }

    public String getGroup() {
        return group;
    }

    public int getHeight() {
        return h;
    }

    public String getSprite() {
        return sprite;
    }

    public int getWidth() {
        return w;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
